package com.malicankaya.kotlinbasics

fun main() {
    //Assignment(+= , -= , *= , /= , %=)
    var a = 5
    a += 5
    println(a)
    a -= 3
    println(a)
    a /= 2
    println(a)
    a *= 3
    println(a)
    a %= 5
    println(a)

    //Increment & Decrement operators (++, --)
    println(a++)
    println(a--)
    println(++a)
    println(--a)

}